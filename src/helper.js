export const getbgColor = (mode) => {
  if (mode === "dark") {
    return "#252E39";
  } else {
    return "white";
  }
};
export const getColor = (mode) => {
  if (mode === "dark") {
    return "white";
  } else {
    return "#252E39";
  }
};
export const getBorder = (mode) => {
  if (mode === "dark") {
    return "";
  } else {
    return "1px solid #ddd";
  }
};
